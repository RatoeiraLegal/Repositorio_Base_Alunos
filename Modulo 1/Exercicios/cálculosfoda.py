#Multiplicação
número1M = float(input("Fala um número para uma multiplicação: "))
número2M = float(input("Fala um segundo número para uma multiplicação: "))
resultadoM = float(número1M * número2M)
print(resultadoM)

#Divisão
número1D = float(input("Fala um número para uma divisão: "))
número2D = float(input("Fala um segundo número para uma divisão: "))
resultadoD = float(número1D / número2D)
print(resultadoD)

#Subtração
número1S = float(input("Fala um número para uma subtração: "))
número2S = float(input("Fala um segundo número para uma subtração: "))
resultadoS = float(número1S - número2S)
print(resultadoS) 